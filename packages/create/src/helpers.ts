/**
 * Checks that the specified DB connection options are working (i.e. a connection can be
 * established) and that the named database exists.
 */
export function checkDbConnection(options: any, root: string): Promise<true> {
    switch (options.type) {
        case 'mysql':
            return checkMysqlDbExists(options, root);
        case 'postgres':
            return checkPostgresDbExists(options, root);
        default:
            return Promise.resolve(true);
    }
}

async function checkMysqlDbExists(options: any, root: string): Promise<true> {
    // Use require.resolve to find the package, which handles npm workspace hoisting
    const mysqlPath = require.resolve('mysql2/promise', { paths: [root] });
    const mysql = await import(mysqlPath);
    const connectionOptions = {
        host: options.host,
        user: options.username,
        password: options.password,
        port: options.port,
        database: options.database,
    };

    let connection;
    try {
        // mysql2/promise's createConnection returns a Promise that resolves
        // to a connection once connected, so we must await it.
        connection = await mysql.createConnection(connectionOptions);
    } catch (err: any) {
        if (err.code === 'ER_BAD_DB_ERROR') {
            throwDatabaseDoesNotExist(options.database);
        }
        throwConnectionError(err);
    }

    await connection.end();
    return true;
}

async function checkPostgresDbExists(options: any, root: string): Promise<true> {
    // Use require.resolve to find the package, which handles npm workspace hoisting
    const pgPath = require.resolve('pg', { paths: [root] });
    const { Client } = await import(pgPath);
    const connectionOptions = {
        host: options.host,
        user: options.username,
        password: options.password,
        port: options.port,
        database: options.database,
        schema: options.schema,
        ssl: options.ssl,
    };
    const client = new Client(connectionOptions);

    try {
        await client.connect();

        const schema = await client.query(
            `SELECT schema_name
             FROM information_schema.schemata
             WHERE schema_name = '${options.schema as string}'`,
        );
        if (schema.rows.length === 0) {
            throw new Error('NO_SCHEMA');
        }
    } catch (e: any) {
        if (e.code === '3D000') {
            throwDatabaseDoesNotExist(options.database);
        } else if (e.message === 'NO_SCHEMA') {
            throwDatabaseSchemaDoesNotExist(options.database, options.schema);
        } else if (e.code === '28000') {
            throwSSLConnectionError(e, options.ssl);
        }
        throwConnectionError(e);
        await client.end();
        throw e;
    }
    await client.end();
    return true;
}

/**
 * Check to see if Docker is installed and running.
 * If not, attempt to start it.
 * If that is not possible, return false.
 *
 * Refs:
 * - https://stackoverflow.com/a/48843074/772859
 */
export async function isDockerAvailable(): Promise<{ result: 'not-found' | 'not-running' | 'running' }> {
    const dockerSpinner = spinner();

    function isDaemonRunning(): boolean {
        try {
            execFileSync('docker', ['stats', '--no-stream'], { stdio: 'ignore' });
            return true;
        } catch (e: any) {
            return false;
        }
    }

    dockerSpinner.start('Checking for Docker');
    try {
        execFileSync('docker', ['-v'], { stdio: 'ignore' });
        dockerSpinner.message('Docker was found!');
    } catch (e: any) {
        dockerSpinner.stop('Docker was not found on this machine. We will use SQLite for the database.');
        return { result: 'not-found' };
    }
    // Now we need to check if the docker daemon is running
    const isRunning = isDaemonRunning();
    if (isRunning) {
        dockerSpinner.stop('Docker is running');
        return { result: 'running' };
    }
    dockerSpinner.message('Docker daemon is not running. Attempting to start');
    // detect the current OS
    const currentPlatform = platform();
    try {
        if (currentPlatform === 'win32') {
            // https://stackoverflow.com/a/44182489/772859
            execSync('"C:\\Program Files\\Docker\\Docker\\Docker Desktop.exe"', { stdio: 'ignore' });
        } else if (currentPlatform === 'darwin') {
            execSync('open -a Docker', { stdio: 'ignore' });
        } else {
            execSync('systemctl start docker', { stdio: 'ignore' });
        }
    } catch (e: any) {
        dockerSpinner.stop('Could not start Docker.');
        log(e.message, { level: 'verbose' });
        return { result: 'not-running' };
    }
    // Verify that the daemon is now running
    let attempts = 1;
    do {
        log(`Checking for Docker daemon... (attempt ${attempts})`, { level: 'verbose' });
        if (isDaemonRunning()) {
            log(`Docker daemon is now running (after ${attempts} attempts).`, { level: 'verbose' });
            dockerSpinner.stop('Docker is running');
            return { result: 'running' };
        }
        await new Promise(resolve => setTimeout(resolve, 50));
        attempts++;
    } while (attempts < 100);
    dockerSpinner.stop('Docker daemon could not be started');
    return { result: 'not-running' };
}

/**
 * Sanitizes a project name into a valid Docker Compose project name
 * (lowercase letters, digits, `-` and `_`, starting with an alphanumeric).
 * Used as the top-level `name` in the generated docker-compose.yml so that each
 * created project gets its own Compose project. Without it, Compose derives the
 * project name from the compose file's directory — which in monorepo mode is
 * always `apps/server`, so every project would collide on the project "server".
 *
 * When sanitization alters the name, a stable suffix derived from the original is
 * appended: otherwise distinct project names could sanitize to the same value
 * (e.g. `shop.v1` and `shopv1`) and end up sharing containers and volumes.
 */
export function toComposeProjectName(name: string): string {
    const sanitized = name
        .toLowerCase()
        .replace(/[^a-z0-9_-]+/g, '-')
        .replace(/^[_-]+/, '');
    if (sanitized === name) {
        return name;
    }
    const suffix = createHash('sha256').update(name).digest('hex').slice(0, 8);
    const base = sanitized.replace(/[_-]+$/, '') || 'vendure';
    return `${base}-${suffix}`;
}

export async function startPostgresDatabase(root: string, projectName: string): Promise<boolean> {
    // Now we need to run the postgres database via Docker
    let containerName: string | undefined;
    const postgresContainerSpinner = spinner();
    postgresContainerSpinner.start('Starting PostgreSQL database');
    try {
        const composePromise = promisify(execFile)(`docker`, [
            `compose`,
            `-f`,
            path.join(root, 'docker-compose.yml'),
            `up`,
            `-d`,
            `postgres_db`,
        ]);
        // `docker compose up` can ask interactive questions (e.g. "Volume exists but
        // doesn't match configuration. Recreate?"). Close stdin so it gets EOF instead
        // of blocking forever on a prompt the user cannot see.
        composePromise.child.stdin?.end();
        const result = await composePromise;
        containerName = result.stderr.match(/Container\s+(.+-postgres_db[^ ]*)/)?.[1];
        if (!containerName) {
            // Fall back to Compose's naming convention: <project>-<service>-<index>
            containerName = `${toComposeProjectName(projectName)}-postgres_db-1`;
            postgresContainerSpinner.message(
                'Could not find container name. Guessing it is: ' + containerName,
            );
            log(pc.red('Could not find container name. Guessing it is: ' + containerName), {
                newline: 'before',
                level: 'verbose',
            });
        } else {
            log(pc.green(`Started PostgreSQL database in container "${containerName}"`), {
                newline: 'before',
                level: 'verbose',
            });
        }
    } catch (e: any) {
        log(pc.red(`Failed to start PostgreSQL database: ${e.message as string}`));
        postgresContainerSpinner.stop('Failed to start PostgreSQL database');
        return false;
    }
    postgresContainerSpinner.message(`Waiting for PostgreSQL database to be ready...`);
    let attempts = 1;
    let isReady = false;
    do {
        // We now need to ensure that the database is ready to accept connections.
        // The `-h localhost` forces a TCP check: during a first boot, initdb runs a
        // temporary server that only listens on the unix socket, and a socket-based
        // pg_isready would report "ready" before the real server is up.
        try {
            const result = execFileSync(`docker`, [
                `exec`,
                `-i`,
                containerName,
                `pg_isready`,
                `-h`,
                `localhost`,
            ]);
            isReady = result?.toString().includes('accepting connections');
            if (!isReady) {
                log(pc.yellow(`PostgreSQL database not yet ready. Attempt ${attempts}...`), {
                    level: 'verbose',
                });
            }
        } catch (e: any) {
            // ignore
            log('is_ready error:' + (e.message as string), { level: 'verbose', newline: 'before' });
        }
        if (isReady) {
            break;
        }
        await new Promise(resolve => setTimeout(resolve, PG_READY_POLL_INTERVAL_MS));
        attempts++;
    } while (attempts <= PG_READY_MAX_ATTEMPTS);
    if (!isReady) {
        postgresContainerSpinner.stop(
            pc.red(
                `PostgreSQL database did not become ready within ${Math.round(
                    (PG_READY_MAX_ATTEMPTS * PG_READY_POLL_INTERVAL_MS) / 1000,
                )} seconds`,
            ),
        );
        log(`Check the container logs with: docker logs ${containerName}`);
        return false;
    }
    postgresContainerSpinner.stop('PostgreSQL database is ready');
    return true;
}

