/**
 * If project only contains files generated by GH, it’s safe.
 * Also, if project contains remnant error logs from a previous
 * installation, lets remove them now.
 * We also special case IJ-based products .idea because it integrates with CRA:
 * https://github.com/facebook/create-react-app/pull/368#issuecomment-243446094
 */
export function isSafeToCreateProjectIn(root: string, name: string) {
    // These files should be allowed to remain on a failed install,
    // but then silently removed during the next create.
    const errorLogFilePatterns = ['npm-debug.log', 'yarn-error.log', 'yarn-debug.log'];
    const validFiles = [
        '.DS_Store',
        'Thumbs.db',
        '.git',
        '.gitignore',
        '.idea',
        'README.md',
        'AGENTS.md',
        'LICENSE',
        '.hg',
        '.hgignore',
        '.hgcheck',
        '.npmignore',
        'mkdocs.yml',
        'docs',
        '.travis.yml',
        '.gitlab-ci.yml',
        '.gitattributes',
        'migration.ts',
        'node_modules',
        'package.json',
        'package-lock.json',
        'src',
        'static',
        'tsconfig.json',
        'yarn.lock',
    ];

    const conflicts = fs
        .readdirSync(root)
        .filter(file => !validFiles.includes(file))
        // IntelliJ IDEA creates module files before CRA is launched
        .filter(file => !/\.iml$/.test(file))
        // Don't treat log files from previous installation as conflicts
        .filter(file => !errorLogFilePatterns.some(pattern => file.indexOf(pattern) === 0));

    if (conflicts.length > 0) {
        log(`The directory ${pc.green(name)} contains files that could conflict:`, { newline: 'after' });
        for (const file of conflicts) {
            log(`  ${file}`);
        }
        log('Either try using a new directory name, or remove the files listed above.', {
            newline: 'before',
        });

        return false;
    }

    // Remove any remnant files from a previous installation
    const currentFiles = fs.readdirSync(path.join(root));
    currentFiles.forEach(file => {
        errorLogFilePatterns.forEach(errorLogFilePattern => {
            // This will catch `(npm-debug|yarn-error|yarn-debug).log*` files
            if (file.indexOf(errorLogFilePattern) === 0) {
                fs.removeSync(path.join(root, file));
            }
        });
    });
    return true;
}

export function scaffoldAlreadyExists(root: string, name: string): boolean {
    const scaffoldFiles = ['migration.ts', 'package.json', 'tsconfig.json', 'README.md'];
    const files = fs.readdirSync(root);
    return scaffoldFiles.every(scaffoldFile => files.includes(scaffoldFile));
}

export function checkNodeVersion(requiredVersion: string, currentVersion: string = process.version) {
    if (!semver.satisfies(currentVersion, requiredVersion)) {
        log(
            pc.red(
                `You are running Node ${currentVersion}.` +
                    `Vendure requires Node ${requiredVersion} or higher.` +
                    'Please update your version of Node.',
            ),
        );
        process.exit(1);
    }
    if (semver.major(currentVersion) < OLDEST_NON_EOL_NODE_MAJOR) {
        log(
            pc.yellow(
                `You are running Node ${currentVersion}, which has reached end-of-life. ` +
                    'Native dependencies may no longer provide prebuilt binaries for it, ' +
                    'which can cause installation failures. Consider upgrading to the ' +
                    'current LTS release: https://nodejs.org/en/download',
            ),
        );
    }
}

export function checkThatNpmCanReadCwd() {
    const cwd = process.cwd();
    let childOutput = null;
    try {
        // Note: intentionally using spawn over exec since
        // the problem doesn't reproduce otherwise.
        // `npm config list` is the only reliable way I could find
        // to reproduce the wrong path. Just printing process.cwd()
        // in a Node process was not enough.
        childOutput = spawn.sync('npm', ['config', 'list']).output.join('');
    } catch (err: any) {
        // Something went wrong spawning node.
        // Not great, but it means we can't do this check.
        // We might fail later on, but let's continue.
        return true;
    }
    if (typeof childOutput !== 'string') {
        return true;
    }
    const lines = childOutput.split('\n');
    // `npm config list` output includes the following line:
    // "; cwd = C:\path\to\current\dir" (unquoted)
    // I couldn't find an easier way to get it.
    const prefix = '; cwd = ';
    const line = lines.find(l => l.indexOf(prefix) === 0);
    if (typeof line !== 'string') {
        // Fail gracefully. They could remove it.
        return true;
    }
    const npmCWD = line.substring(prefix.length);
    if (npmCWD === cwd) {
        return true;
    }
    log(
        pc.red(
            'Could not start an npm process in the right directory.\n\n' +
                `The current directory is: ${pc.bold(cwd)}\n` +
                `However, a newly started npm process runs in: ${pc.bold(npmCWD)}\n\n` +
                'This is probably caused by a misconfigured system terminal shell.',
        ),
    );
    if (process.platform === 'win32') {
        log(
            pc.red('On Windows, this can usually be fixed by running:\n\n') +
                `  ${pc.cyan('reg')} delete "HKCU\\Software\\Microsoft\\Command Processor" /v AutoRun /f\n` +
                `  ${pc.cyan(
                    'reg',
                )} delete "HKLM\\Software\\Microsoft\\Command Processor" /v AutoRun /f\n\n` +
                pc.red('Try to run the above two lines in the terminal.\n') +
                pc.red(
                    'To learn more about this problem, read: https://blogs.msdn.microsoft.com/oldnewthing/20071121-00/?p=24433/',
                ),
        );
    }
    return false;
}

/**
 * Detects the package manager used to invoke this CLI from the
 * `npm_config_user_agent` env var that npm/yarn/pnpm/bun all set when running a
 * package's bin. Falls back to npm. This is what lets `bunx @vendure/create`
 * (and the pnpm/yarn equivalents) install with a package manager that is
 * actually on the PATH, rather than hard-failing on a missing `npm` (issue #4390).
 */
export function detectPackageManager(
    userAgent: string | undefined = process.env.npm_config_user_agent,
): PackageManager {
    const name = userAgent?.split(' ')[0]?.split('/')[0];
    if (name === 'yarn' || name === 'pnpm' || name === 'bun') {
        return name;
    }
    return 'npm';
}

/**
 * Builds the package-manager-specific command and args for installing the given
 * dependencies (exact versions, optionally as devDependencies).
 */
export function getInstallCommand(
    packageManager: PackageManager,
    options: { dependencies: string[]; isDevDependencies?: boolean; logLevel: CliLogLevel },
): { command: string; args: string[] } {
    const { dependencies, isDevDependencies = false, logLevel } = options;
    // With no explicit packages, the intent is "install everything declared in the
    // manifest" (e.g. the downloaded storefront). `npm install` does this with no args,
    // but `<pm> add` with no args is an error for yarn/pnpm/bun — so use the plain
    // `install` subcommand, which all four managers accept.
    if (dependencies.length === 0) {
        return { command: packageManager, args: ['install'] };
    }
    switch (packageManager) {
        case 'yarn':
            return {
                command: 'yarn',
                args: ['add', '--exact', ...(isDevDependencies ? ['--dev'] : []), ...dependencies],
            };
        case 'pnpm':
            return {
                command: 'pnpm',
                args: ['add', '--save-exact', ...(isDevDependencies ? ['--save-dev'] : []), ...dependencies],
            };
        case 'bun':
            return {
                command: 'bun',
                args: ['add', '--exact', ...(isDevDependencies ? ['--dev'] : []), ...dependencies],
            };
        default:
            return {
                command: 'npm',
                args: [
                    'install',
                    '--save',
                    '--save-exact',
                    '--loglevel',
                    'error',
                    ...(isDevDependencies ? ['--save-dev'] : []),
                    ...(logLevel === 'verbose' ? ['--verbose'] : []),
                    ...dependencies,
                ],
            };
    }
}

/**
 * Manager-specific command fragments used to generate package.json scripts, CLI
 * instructions, Dockerfiles and READMEs that match the package manager the project
 * was created with. This is the single source of truth for everything that used to
 * be hard-coded as `npm`.
 */
export interface PackageManagerInfo {
    /** The package manager binary, e.g. `npm` | `yarn` | `pnpm` | `bun`. */
    name: PackageManager;
    /** Prefix to run a package.json script, e.g. `npm run` / `pnpm run` / `bun run`. */
    runScript: string;
    /** Prefix to execute a locally-installed bin, e.g. `npx` / `bunx` / `pnpm exec`. */
    exec: string;
    /** Install all deps declared in the manifest, e.g. `npm install` / `pnpm install`. */
    install: string;
    /** Reproducible, lockfile-frozen install for CI & Docker (includes devDependencies). */
    ciInstall: string;
    /** The lockfile this manager writes. */
    lockfile: string;
    /**
     * Whether the manager reads the package.json `workspaces` field (npm/yarn/bun) or
     * requires a `pnpm-workspace.yaml` (pnpm).
     */
    usesPackageJsonWorkspaces: boolean;
    /** Build a command to run a script in a named workspace, e.g. `npm run dev -w server`. */
    workspaceScript(workspace: string, script: string): string;
}

export function getPackageManagerInfo(packageManager: PackageManager): PackageManagerInfo {
    switch (packageManager) {
        case 'yarn':
            // `yarn install` works on both Yarn Classic and Berry (and is immutable by
            // default under CI on Berry); `--immutable`/`--frozen-lockfile` differ between
            // the two, so we avoid them. Yarn is best-effort and not covered by CI.
            return {
                name: 'yarn',
                runScript: 'yarn run',
                exec: 'yarn',
                install: 'yarn install',
                ciInstall: 'yarn install',
                lockfile: 'yarn.lock',
                usesPackageJsonWorkspaces: true,
                workspaceScript: (workspace, script) => `yarn workspace ${workspace} ${script}`,
            };
        case 'pnpm':
            return {
                name: 'pnpm',
                runScript: 'pnpm run',
                exec: 'pnpm exec',
                install: 'pnpm install',
                ciInstall: 'pnpm install --frozen-lockfile',
                lockfile: 'pnpm-lock.yaml',
                usesPackageJsonWorkspaces: false,
                workspaceScript: (workspace, script) => `pnpm --filter ${workspace} ${script}`,
            };
        case 'bun':
            return {
                name: 'bun',
                runScript: 'bun run',
                exec: 'bunx',
                install: 'bun install',
                ciInstall: 'bun install --frozen-lockfile',
                lockfile: 'bun.lock',
                usesPackageJsonWorkspaces: true,
                workspaceScript: (workspace, script) => `bun run --filter ${workspace} ${script}`,
            };
        default:
            return {
                name: 'npm',
                runScript: 'npm run',
                exec: 'npx',
                install: 'npm install',
                ciInstall: 'npm ci',
                lockfile: 'package-lock.json',
                usesPackageJsonWorkspaces: true,
                workspaceScript: (workspace, script) => `npm run ${script} -w ${workspace}`,
            };
    }
}

